#Resampling

#Los procedimientos basados en remuestreo son formas de realizar inferencias estadísticas basadas en la población, mientras se vive dentro de nuestros datos. 

#Los científicos de datos tienden a gustar realmente las muestras basadas en inferencias, ya que son procedimientos muy centrados en los datos, se adaptan bien a grandes estudios y, a menudo, hacen muy pocas suposiciones.

