#Power

#Hemos hablado de un error de Tipo I, rechazando la hipótesis nula cuando es cierta. Hemos estructurado nuestra prueba de hipótesis para que la probabilidad de que esto ocurra sea pequeña. El otro tipo de error que podríamos cometer es no rechazar cuando la alternativa es verdadera (error de Tipo II). O podríamos pensar en la probabilidad de rechazar el nulo cuando es falso. 

#Esto se llama Power = 1 - Error tipo II. 

#No tenemos tanto control sobre esta probabilidad, ya que hemos gastado toda nuestra flexibilidad para garantizar que la tasa de error Tipo I sea pequeña.

#Una vía para el control del poder está en la fase de diseño. Allí, suponiendo que nuestras finanzas nos lo permitan, podemos elegir un tamaño de muestra lo suficientemente grande como para que podamos rechazarlo si la alternativa es cierta. Por lo tanto, el uso más frecuente del power es ayudarnos a diseñar estudios.

