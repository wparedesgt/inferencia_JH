#04 Independencia

#Cubramos brevemente el tema de la independencia. 

#Aludimos a esto anteriormente, en el sentido de que dijimos que A es independiente de B si la probabilidad de que A sea B dada es igual a la probabilidad A para un evento B que tiene probabilidad positiva. Otra definición de independencia es que el problema, A y B son independientes si la probabilidad de que A se cruza con B resulta ser el producto de sus probabilidades.

#Esto entonces comienza una lección muy importante. No se puede simplemente multiplicar las probabilidades de cualquier manera. 

#Tienes que multiplicar las probabilidades independientes probadas asociadas con los eventos de independencia. Veamos un ejemplo numérico. 

#¿Cuál es la probabilidad de obtener dos caras consecutivas? 

#Bueno, definamos A como la probabilidad de obtener una cabeza en la primera vuelta y B como la probabilidad de obtener una cabeza en la segunda vuelta. 

#Ambas probabilidades son 0.5. En este caso, estamos asumiendo una moneda justa.

#Una intersección B es el evento de obtener caras en los lanzamientos uno y dos. En este caso, debido a que los eventos son independientes, la probabilidad de que A se cruce con B es el producto de las probabilidades. Y entonces diríamos que es 0.25.

#La gente hace este cálculo de forma bastante natural. El problema es cuando las personas multiplican las probabilidades cuando no deberían. 

#Una instancia particularmente cruda de probabilidades de multiplicación incorrecta ocurrió en el volumen 309 de la ciencia, que informaba sobre un médico que estaba en juicio por dar testimonio experto en un juicio penal. 

#El juicio penal involucró a una madre que tuvo dos hijos que murieron de síndrome de muerte súbita infantil. El testimonio experto tomó la prevalencia de siete, en el síndrome de muerte súbita del lactante, como uno de cada 8,500, y luego los multiplicó para encontrar la probabilidad de que dos niños tuvieran el síndrome de muerte súbita del lactante con la misma madre.

#Basado en esta evidencia, y presumo otra evidencia, la madre en el juicio fue condenado por asesinato. Relevante para esta clase, el error principal fue multiplicar las probabilidades de eventos que no eran necesariamente independientes. 

#Y tiene sentido que en este caso se asuma que los eventos serían dependientes porque proceso que tiene un componente ambiental genético o familiar es probable que sea dependiente dentro de las familias. En esta clase, la forma principal en que usaremos la independencia es asumiendo que una colección de variables aleatorias es independiente y de la misma distribución. 

#Recordemos tales variables aleatorias independientes e idénticamente distribuidas. Por lo tanto, puede pensar en varios lanzamientos de monedas como IID porque son independientes. 

#Un lanzamiento de moneda es independiente de la siguiente. Y todos se elevan desde la misma distribución, 0.5 para cabezas y 0.5 para colas.

#El muestreo IID es nuestro modelo predeterminado para una muestra aleatoria. Entonces, ya sea que tengamos una muestra aleatoria real o no, a menudo usamos muestreo aleatorio o I, el modelo de IID como modelo conceptual para analizar nuestros datos. 

